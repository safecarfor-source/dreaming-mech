/**
 * 모든 유효한 서비스-지역 slug 조합 반환
 */
export function getAllServiceRegionSlugs(): string[] {
  return SERVICES.flatMap((s) =>
    REGIONS.map((r) => `${s.slug}-${r.slug}`),
  );
}

/**
 * slug에서 서비스/지역 정보 추출
 */
export function parseServiceRegionSlug(slug: string): {
  service: ServiceInfo | null;
  region: RegionInfo | null;
  content: ServiceRegionContent | null;
} {
  for (const service of SERVICES) {
    for (const region of REGIONS) {
      const key = `${service.slug}-${region.slug}`;
      if (slug === key) {
        return {
          service,
          region,
          content: SERVICE_REGION_CONTENT[key] || null,
        };
      }
    }
  }
  return { service: null, region: null, content: null };
}
